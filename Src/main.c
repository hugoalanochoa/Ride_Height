/**
** TIM5 Init function
**
** \details
**
** \return    Nothing
**************************************************************************************/
void MX_TIM5_Init(void)
{

  TIM_MasterConfigTypeDef sMasterConfig;
  TIM_OC_InitTypeDef sConfigOC;

  htim5.Instance = TIM5;
  htim5.Init.Prescaler = 83;
  htim5.Init.CounterMode = TIM_COUNTERMODE_UP;
  htim5.Init.Period = 0xFFFFFFFF;
  htim5.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
  HAL_TIM_OC_Init(&htim5);

  sMasterConfig.MasterOutputTrigger = TIM_TRGO_RESET;
  sMasterConfig.MasterSlaveMode = TIM_MASTERSLAVEMODE_DISABLE;
  HAL_TIMEx_MasterConfigSynchronization(&htim5, &sMasterConfig);

  sConfigOC.OCMode = TIM_OCMODE_TIMING;
  sConfigOC.Pulse = 0;
  sConfigOC.OCPolarity = TIM_OCPOLARITY_HIGH;
  sConfigOC.OCFastMode = TIM_OCFAST_DISABLE;
  HAL_TIM_OC_ConfigChannel(&htim5, &sConfigOC, TIM_CHANNEL_1);




}


void TimeStamp_Init(){
    MX_TIM5_Init();
}

void TimeStamp_Reset(){
    HAL_TIM_Base_Start(&htim5);
    htim5.Instance->CNT=0;

}

uint32_t TimeStamp_Get(){
    return htim5.Instance->CNT;
}



uint32_t
filterByWeight(
               uint32_t new, /**< Value of new measurement */
               uint32_t curr, /**< Current filtered value, (stored or static  filtered value) */
               uint8_t weight) /**< Percent of new value */

/** Filter by weight (percentage).
 **
 ** \return New filtered value.
 **************************************************************************************/
{
   return (uint32_t) ((((uint32_t) curr * (100UL - weight)) + ((uint32_t) new * weight)) / 100UL);
}

